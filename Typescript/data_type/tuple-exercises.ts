// 元组（Tuple）是 TypeScript 中一种特殊的数组类型，它可以存储不同类型的元素，
// 并且每个位置的元素类型都是固定的。
//
// 什么时候使用元组？
// - 当你需要存储固定数量、不同类型的数据时
// - 当数据的位置和类型都有特定含义时
// - 比如坐标 (x, y)、RGB 颜色 (r, g, b) 等
//
// 与普通数组的区别：
// - 数组：所有元素类型相同 [number, number, number]
// - 元组：每个位置类型固定 [string, number, boolean]

