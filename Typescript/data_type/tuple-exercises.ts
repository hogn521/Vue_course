// ============================================
// 总结和最佳实践
// ============================================
//
// 🎯 核心要点：
// 1. 元组适合存储固定数量、类型各异的数据
// 2. 元组的每个位置都有明确的类型约束
// 3. 可选元素 `type?` 只能出现在元组末尾
// 4. 剩余元素 `...type[]` 允许可变长度
// 5. 元组支持解构，便于提取数据
// 6. 元组可以使用数组方法，但有类型限制
// 7. 使用 `as const` 可以创建真正的只读元组
//
// ⚠️ 常见陷阱：
// 1. 混淆数组类型和元组类型
// 2. 可选元素位置错误（必须在最后）
// 3. 剩余元素位置错误（必须在最后）
// 4. 忽视元组的可变性（可以使用 push 等方法修改）
// 5. 过度使用元组导致代码难以维护
//
// ✅ 最佳实践：
// 1. 只在数据结构固定且明确时使用元组
// 2. 为元组类型创建类型别名，提高可读性
// 3. 考虑使用接口或对象替代复杂的元组
// 4. 使用 `readonly` 或 `as const` 保护重要数据
// 5. 在元组元素超过 3-4 个时，考虑使用对象
//
// 🤔 何时使用元组 vs 对象？
// 使用元组：
// - 数据结构简单且固定
// - 元素位置有明确含义（如坐标、RGB）
// - 需要数组方法的便利性
//
// 使用对象：
// - 数据结构复杂或可能变化
// - 需要具名属性提高可读性
// - 属性数量较多或可选属性较多
